vim.opt.clipboard = "unnamedplus" -- yank goes to clipboard

vim.opt.laststatus = 3 -- Always show a single global statusline

vim.opt.number = true -- absolute line number of current line
vim.opt.relativenumber = true -- relative line number 

vim.opt.tabstop = 2      -- Number of spaces a <Tab> counts for
vim.opt.softtabstop = 2  -- Number of spaces a <Tab> counts for while editing
vim.opt.shiftwidth = 2   -- Number of spaces for each step of (auto)indent
vim.opt.expandtab = true -- Convert tabs to spaces

